package org.assignment.programs;

public class MultiplicationTableOf4 {
	/**
	 * Write a JAVA program to print the Multiplication Table of 4
	 * 
	 * @param args
	 */
	public static void main(String[] args) {
		int j = 4;
		for (int i = 1; i <= 10; i++)
			System.out.println(j + " X " + i + " = " + (j * i));
	}
}
